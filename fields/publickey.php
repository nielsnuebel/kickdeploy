defined('JPATH_PLATFORM') or die;

jimport('joomla.filesystem.file');

/**
 * Form Field class for the Joomla Platform.
 * Supports a one line text field.
 *
 * @link   http://www.w3.org/TR/html-markup/input.text.html#input.text
 * @since  11.1
 */
class JFormFieldPublicKey extends JFormField
{
	/**
	 * The form field type.
	 *
	 * @var    string
	 * @since  11.1
	 */
	protected $type = 'PublicKey';

	/**
	 * Method to get the field input markup.
	 *
	 * @return  string  The field input markup.
	 *
	 * @since   11.1
	 */
	protected function getInput()
	{
		$html[] = '<textarea cols="1" rows="1" readonly class="span8"> ' . __DIR__. '</textarea><br><br>';
		$html[] = '<input type="text" name="' . $this->name . '" id="' . $this->id . '" value="'
			. htmlspecialchars($this->value, ENT_COMPAT, 'UTF-8') . '"/>';

		if (JFile::exists($this->value))
		{
			$publickey = file_get_contents($this->value);

			$html[] = '<br><br><textarea cols="10" rows="5" class="span8" readonly> ' . $publickey. '</textarea>';
		}

		return implode($html);
	}
}
